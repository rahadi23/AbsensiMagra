public class Maba {
    private String nomorPendaftaran;
    private String nama;
    private String pathFoto;
    private String noHp;
    private String asalProp;
    private String email;

    public String getNomorPendaftaran() {
        return nomorPendaftaran;
    }

    public void setNomorPendaftaran(String nomorPendaftaran) {
        this.nomorPendaftaran = nomorPendaftaran;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getPathFoto() {
        return pathFoto;
    }

    public void setPathFoto(String pathFoto) {
        this.pathFoto = pathFoto;
    }

    public String getNoHp() {
        return noHp;
    }

    public void setNoHp(String noHp) {
        this.noHp = noHp;
    }

    public String getAsalProp() {
        return asalProp;
    }

    public void setAsalProp(String asalProp) {
        this.asalProp = asalProp;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
